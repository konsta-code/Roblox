PREVIEW_DIR = os.path.join(ROOT, "previews")
BLEND_PATH = os.path.join(ROOT, "CTFGame_ClassArmor_v01.blend")
REPORT_PATH = os.path.join(ROOT, "class_armor_report.json")
SKIP_RENDER = "--skip-render" in sys.argv
RENDER_FILTER = next((arg.split("=", 1)[1] for arg in sys.argv if arg.startswith("--render-filter=")), None)
os.makedirs(EXPORT_DIR, exist_ok=True)
os.makedirs(PREVIEW_DIR, exist_ok=True)

# Outputs are entirely generated by this script. Remove armor files from the
# previous art direction so a rebuilt package can never mix both factions.
generated_patterns = []
if RENDER_FILTER is None:
    generated_patterns.extend((
        os.path.join(EXPORT_DIR, "*_Armor.fbx"),
        os.path.join(PREVIEW_DIR, "*_Armor.png"),
    ))
for generated_pattern in generated_patterns:
    for generated_path in glob.glob(generated_pattern):
        os.remove(generated_path)

